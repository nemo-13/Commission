package User;

/**
 * Абстрактный класс фабрики пользователей.
 * Определяет фабричный метод createUser(), который должны реализовать подклассы.
 */
public abstract class UserFactory {
    /**
     * Фабричный метод для создания пользователей.
     * Конкретные подклассы определяют, как именно создается пользователь.
     *
     * @return созданный объект пользователя
     */
    public abstract User createUser();
}